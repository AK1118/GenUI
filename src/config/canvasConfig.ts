import ImageToolkit from "../image-toolkit";
import Painter from "../painter";

class CanvasConfig {
  private kit: ImageToolkit;
  private paint: Painter;
  public init(kit: ImageToolkit) {
    this.kit = kit;
  }
  public get rect() {
    return this.kit.getCanvasRect;
  }
  //所有图层对象
  public get views() {
    return this.kit.getViewObjects;
  }
  public setGlobalPaint(paint: Painter) {
    this.paint = paint;
  }
  public get globalPaint():Painter{
    return this.paint;
  }
}

export default new CanvasConfig();
